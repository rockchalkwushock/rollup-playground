/* @flow */

/**
 * A function for finding the sum
 * between two numbers.
 *
 * @param {Number} a
 * @param {Number} b
 * @returns {Number}
 */
export const sum = (a: number, b: number): number => a + b;

/**
 * A function for finding the difference
 * between two numbers.
 *
 * @param {Number} a
 * @param {Number} b
 * @returns {Number}
 */
export const subtract = (a: number, b: number): number => a - b;

/**
 * A function for finding the product
 * between two numbers.
 *
 * @param {Number} a
 * @param {Number} b
 * @returns {Number}
 */
export const multiply = (a: number, b: number): number => a * b;

/**
 * A function for finding the quotient
 * between two numbers.
 *
 * @param {Number} a
 * @param {Number} b
 * @returns {Number}
 */
export const divide = (a: number, b: number): number => a / b;
